package com.nyutiz.boatgame;

public class Boat {
    public String name;
    public int x;
    public int y;
    public int speed;
    public double angle;

    public Boat(String name, int x, int y, int speed, double angle) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.speed = speed;
        this.angle = angle;
    }

}
